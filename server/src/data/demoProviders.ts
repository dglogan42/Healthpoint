export function haversineKm(
  lat1: number,
  lng1: number,
  lat2: number,
  lng2: number,
): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

function matchesServiceType(provider: Provider, serviceType: string): boolean {
  if (provider.communityServiceType === serviceType) return true;
  const typeConfig = getCommunityServiceType(serviceType);
  if (!typeConfig) return false;
  const haystack = [
    provider.name,
    provider.communityServiceLabel ?? "",
    ...(provider.services ?? []),
  ]
    .join(" ")
    .toLowerCase();
  return typeConfig.keywords.some((kw) => haystack.includes(kw));
}

export function searchDemoProviders(
  lat: number,
  lng: number,
  radiusKm: number,
  category?: string,
  query?: string,
  mode?: LocatorMode,
  serviceType?: string,
): Provider[] {
  const q = query?.toLowerCase().trim();

  return DEMO_PROVIDERS.filter((p) => {
    if (mode === "community" && !COMMUNITY_CATEGORIES.has(p.category)) return false;
    if (category && p.category !== category) return false;
    if (serviceType && !matchesServiceType(p, serviceType)) return false;
    if (q) {
      const haystack = [
        p.name,
        p.address,
        p.suburb,
        p.city,
        p.categoryLabel,
        p.communityServiceLabel ?? "",
        ...(p.services ?? []),
        ...(p.assistancePrograms ?? []),
        ...(p.availability ?? []),
        ...(p.phones?.map((ph) => ph.label) ?? []),
      ]
        .join(" ")
        .toLowerCase();
      if (!haystack.includes(q)) return false;
    }
    return true;
  })
    .map((p) => ({
      ...p,
      distanceKm: haversineKm(lat, lng, p.lat, p.lng),
    }))
    .filter((p) => (p.distanceKm ?? 0) <= radiusKm)
    .sort((a, b) => {
      const priority = (p: Provider) =>
        (p.isEssential ? 0 : 1) +
        (p.availability?.includes("24h") ? 0 : p.availability?.includes("urgent") ? 0.5 : 1);
      const prioDiff = priority(a) - priority(b);
      if (prioDiff !== 0) return prioDiff;
      return (a.distanceKm ?? 0) - (b.distanceKm ?? 0);
    });
}